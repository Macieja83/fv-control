function h(at: string, actor: string, action: string, detail?: string): AuditEntry {
  return { id: crypto.randomUUID(), at, actor, action, detail }
}

/** YYYY-MM-DD w bieżącym miesiącu — domyślne filtry Raportów (ten miesiąc) od razu pokazują demo. */
function ymdThisMonth(day: number): string {
  const d = new Date()
  const y = d.getFullYear()
  const m = d.getMonth() + 1
  return `${y}-${String(m).padStart(2, '0')}-${String(Math.min(Math.max(day, 1), 28)).padStart(2, '0')}`
}

/** ISO z-suffix dla pól `created_at` / `updated_at` (bez template `}T08…` — łatwo pomylić cudzysłów). */
function demoTs(day: number, hour: number, minute = 0): string {
  return (
    ymdThisMonth(day) +
    'T' +
    String(hour).padStart(2, '0') +
    ':' +
    String(minute).padStart(2, '0') +
    ':00Z'
  )
}

